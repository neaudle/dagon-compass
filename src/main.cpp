void setup() {
  Wire.begin(4, 5); // SDA=D2 (GPIO4), SCL=D1 (GPIO5)
  display.clearDisplay();
  display.setTextColor(SSD1306_WHITE);
}

String getDirection(int angle) {
  const char* directions[] = {"N", "NNE", "NE", "ENE", "E", "ESE", "SE", "SSE", "S", "SSW", "SW", "WSW", "W", "WNW", "NW", "NNW", "N"};
  return directions[(int)((angle + 11.25) / 22.5)];
}

void loop() {
  // read the heading
  Wire.beginTransmission(CMPS14_ADDRESS);
  Wire.write(1); 
  Wire.endTransmission();
  Wire.requestFrom(CMPS14_ADDRESS, 1);
  int angle = map(Wire.read(), 0, 255, 0, 359);

  display.clearDisplay();

  // select sprite
  uint8_t spriteIndex = (angle + 22) / 45;  // 0â€“7
  spriteIndex %= 8;
  const unsigned char* currentSprite = dragonSprites[spriteIndex];

  // draw sprite (right side)
  display.drawBitmap(64, 0, currentSprite, 64, 64, WHITE);

  // draw text info (left side)
  display.setTextSize(1);
  display.setCursor(0, 5);
  display.print("HEADING");

  display.setTextSize(2);
  display.setCursor(0, 18);
  display.print(angle);
  display.print((char)247); 

  display.setTextSize(3);
  display.setCursor(0, 40);
  display.print(getDirection(angle));

  display.display();
  delay(100);
}

